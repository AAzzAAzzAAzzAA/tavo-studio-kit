export async function buildTavoAssets(options: BuildAssetsOptions = {}): Promise<BuiltAssets> {
  const now = options.now ?? new Date();
  const suiteId = options.suiteId ?? `codex-devkit-${stamp(now)}`;
  const outDir = path.resolve(options.outDir ?? defaultOutDir);
  await ensureDir(outDir);

  const assets = [
    { kind: "character", filename: `${suiteId}.card.json`, data: makeCharacterCard(suiteId), schema: CharaCardV3Schema },
    { kind: "worldbook", filename: `${suiteId}.worldbook.json`, data: makeWorldbook(suiteId), schema: WorldbookImportSchema },
    { kind: "regex", filename: `${suiteId}.regex.json`, data: makeRegexImport(suiteId), schema: RegexImportSchema },
    { kind: "preset", filename: `${suiteId}.preset.json`, data: makePreset(suiteId), schema: PresetImportSchema }
  ];

  const validation: Array<{ kind: string; ok: boolean; message: string }> = [];
  const files: BuiltAssets["files"] = [];
  for (const asset of assets) {
    const result = asset.schema.safeParse(asset.data);
    validation.push({
      kind: asset.kind,
      ok: result.success,
      message: result.success ? "ok" : result.error.issues.map((issue) => issue.message).join("; ")
    });
    if (!result.success) throw new Error(`${asset.kind} schema validation failed: ${validation.at(-1)?.message}`);
    const file = path.join(outDir, asset.filename);
    await writeJson(file, asset.data);
    files.push({ kind: asset.kind, path: file, sha256: await sha256(file) });
  }

  const arFile = path.join(outDir, `${suiteId}.ar-direct.html`);
  await writeText(arFile, makeDirectArHtml(suiteId));
  files.push({ kind: "ar-direct-html", path: arFile, sha256: await sha256(arFile) });

  const widget = await buildArWidgetHtml({ suiteId });
  const widgetFile = path.join(outDir, `${suiteId}.ar-widget.html`);
  await writeText(widgetFile, widget.html);
  files.push({ kind: "ar-widget-html", path: widgetFile, sha256: await sha256(widgetFile) });

  const widgetRegex = makeArWidgetRegex(suiteId, widget.html);
  const widgetRegexResult = RegexImportSchema.safeParse(widgetRegex);
  validation.push({
    kind: "ar-widget-regex",
    ok: widgetRegexResult.success,
    message: widgetRegexResult.success ? "ok" : widgetRegexResult.error.issues.map((issue) => issue.message).join("; ")
  });
  if (!widgetRegexResult.success) throw new Error(`${validation.at(-1)?.kind} schema validation failed: ${validation.at(-1)?.message}`);
  const widgetRegexFile = path.join(outDir, `${suiteId}.ar-widget.regex.json`);
  await writeJson(widgetRegexFile, widgetRegex);
  files.push({ kind: "ar-widget-regex", path: widgetRegexFile, sha256: await sha256(widgetRegexFile) });

  const manifest = {
    suiteId,
    generatedAt: now.toISOString(),
    files,
    validation
  };
  ManifestSchema.parse(manifest);
  const manifestFile = path.join(outDir, `${suiteId}.manifest.json`);
  await writeJson(manifestFile, manifest);
  files.push({ kind: "manifest", path: manifestFile, sha256: await sha256(manifestFile) });

  const reportFile = path.join(outDir, `${suiteId}.test-report.json`);
  await writeJson(reportFile, {
    suiteId,
    generatedAt: now.toISOString(),
    status: "static pass",
    notes: [
      "Generated from tavo-complete official API boundary and local import fixtures.",
      "Emulator verification is recorded separately under reports/latest."
    ]
  });
  files.push({ kind: "test-report", path: reportFile, sha256: await sha256(reportFile) });

  return { outDir, suiteId, files };
}
