export function makeDirectArHtml(suiteId: string): string {
  return `<div id="${suiteId}-ar-status" style="margin:8px 0;padding:8px 10px;border:1px solid #d99;border-radius:8px;color:#933;background:#fff3f3;font-size:13px;line-height:1.5;">${suiteId}: AR JS 未执行</div>
<script>
(async function(){
  var id=${JSON.stringify(suiteId)};
  var el=document.getElementById(id+'-ar-status');
  function say(t,ok){if(el){el.textContent=id+': '+t;el.style.color=ok?'#27632a':'#933';el.style.borderColor=ok?'#8bc58f':'#d99';el.style.background=ok?'#f1fff2':'#fff3f3';}}
  try{
    if(!(window.tavo&&tavo.set&&tavo.get&&tavo.memory&&tavo.memory.current&&tavo.memory.update)){say('找不到完整 tavo JS API',false);return;}
    tavo.set('devkit.arDirect', 'ok', 'chat');
    var mem=await tavo.memory.current();
    if(!mem){say('当前聊天没有 memory 对象',false);return;}
    if(!Array.isArray(mem.memories)) mem.memories=[];
    var item='['+id+'] AR_DIRECT_OK';
    mem.memories=mem.memories.filter(function(x){return String(x).indexOf('['+id+'] AR_DIRECT_OK')!==0;});
    mem.memories.push(item);
    mem.enabled=true;
    await tavo.memory.update(mem);
    try{tavo.utils.toast(id+' AR direct ok');}catch(_){}
    say('AR JS 已执行并写入长记忆',true);
  }catch(e){say('JS 报错: '+(e&&e.message?e.message:e),false);}
})();
</script>`;
}

